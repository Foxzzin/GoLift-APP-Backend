async function seed() {
  const conn = await mysql.createConnection({
    host:     process.env.DB_HOST     || "127.0.0.1",
    user:     process.env.DB_USER     || "root",
    password: process.env.DB_PASS     || "",
    database: process.env.DB_NAME     || "golift",
  });

  console.log(`\n🚀 A iniciar seed de exercícios PT...`);

  // 1. Limpar tabela
  await conn.query("SET FOREIGN_KEY_CHECKS = 0");
  await conn.query("TRUNCATE TABLE exercicios");
  await conn.query("SET FOREIGN_KEY_CHECKS = 1");
  console.log("✅ Tabela exercicios limpa.");

  // 2. Inserir em batches de 50
  const BATCH = 50;
  let inserted = 0;
  for (let i = 0; i < exercicios.length; i += BATCH) {
    const batch = exercicios.slice(i, i + BATCH);
    const values = batch.map(e => [e.nome, e.grupo_tipo, e.sub_tipo]);
    const [result] = await conn.query(
      "INSERT INTO exercicios (nome, grupo_tipo, sub_tipo) VALUES ?",
      [values]
    );
    inserted += result.affectedRows;
    process.stdout.write(`\r   Inseridos: ${inserted}/${exercicios.length}`);
  }

  await conn.end();
  console.log(`\n✅ Seed concluído! ${inserted} exercícios inseridos.`);
  process.exit(0);
}

seed().catch(err => {
  console.error("\n❌ Erro no seed:", err.message);
  process.exit(1);
});
